// история предсказаний (последние 2)
let history = [];
let count = 0;

// получаем элементы
const button = document.getElementById("btn");
const output = document.getElementById("prediction");
const counter = document.getElementById("counter");

// обработка клика
button.addEventListener("click", () => {
  let index;

  // выбираем случайный индекс, которого не было недавно
  do {
    index = Math.floor(Math.random() * fortunes.length);
  } while (history.includes(index));

  // сохраняем индекс в историю
  history.push(index);

  // оставляем только последние 2
  if (history.length > 2) {
    history.shift();
  }

  // выводим предсказание
  output.textContent = fortunes[index];
  // счетчик предсказаний
  count++;
  counter.textContent = count;
});

